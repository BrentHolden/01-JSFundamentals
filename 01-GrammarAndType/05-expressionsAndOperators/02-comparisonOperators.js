3 == 3

//works with strings and/or variables//

3 != 4

3 === 3 //will return true, because they match in value and in type

3 === '3' //will return false, because they do not match type (one number, one string)

4 > 3 //less/greater-than does not check data type

2 === 2 && 3 !== 4  //returns true if both statements are true, returns false if either is false. If first statement is false, the second statement will not be checked.

'2' === 2 || 3 !== 4 //returns true if either statement is true